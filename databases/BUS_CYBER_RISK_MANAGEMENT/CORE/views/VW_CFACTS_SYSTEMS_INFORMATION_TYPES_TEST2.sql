on APP_CFACTS.SHARED.SEC_VW_INFORMATION_TYPES.contentId = APP_CFACTS.SHARED.SEC_VW_AUTHORIZATION_PACKAGE.tracking_id
*/
WHERE APP_CFACTS.SHARED.SEC_VW_AUTHORIZATION_PACKAGE.IDUID IS NOT NULL
